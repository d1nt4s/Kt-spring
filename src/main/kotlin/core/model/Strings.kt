package core.model

/**
 * Extension-функция для String: делает из строки "slug".
 *
 * - убирает пробелы по краям
 * - переводит в нижний регистр
 * - заменяет всё, что не буквы/цифры, на "-"
 * - убирает лишние дефисы с краёв
 */
fun String.slugify(): String =
    this.trim() // убираем пробелы в начале и конце
        .lowercase() // делаем все буквы маленькими
        .replace("[^a-z0-9]+".toRegex(), "-") // все "плохие символы" -> дефис
        .trim('-') // убираем дефисы с краёв
